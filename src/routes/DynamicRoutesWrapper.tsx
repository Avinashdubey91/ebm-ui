import React, { useMemo, useEffect } from 'react';
import { useMenuData } from '../features/dashboard/hooks/useMenuData';
import { generateRoutes } from '../utils/generateRoutes';
import { useRoutes } from 'react-router-dom';

const DynamicRoutesWrapper: React.FC = () => {
  const { menus, loading } = useMenuData();

  // âœ… Log what useMenuData returned
  useEffect(() => {
    console.log("ðŸŒ€ useMenuData loading:", loading);
    console.log("ðŸ“¦ Menus from DB:", menus);
  }, [menus, loading]);

  const dynamicRoutes = useMemo(() => {
    const routes = loading ? [] : generateRoutes(menus);
    console.log("ðŸ§­ Routes generated by generateRoutes():", routes);

    routes.forEach(route => {
      console.log(`â–¶ï¸ Parent Route: /${route.path}`);
      if (route.children?.length) {
        route.children.forEach(child => {
          console.log(`   â””â”€â”€ /${route.path}/${child.path}`);
        });
      }
    });

    return routes;
  }, [menus, loading]);

  const element = useRoutes(dynamicRoutes);

  if (loading) {
    return <div className="text-center p-4">Loading routes...</div>;
  }

  return element ?? <div className="alert alert-danger text-center m-5 fw-bold fs-3">ðŸš« No Routes Available</div>;
};

export default DynamicRoutesWrapper;
